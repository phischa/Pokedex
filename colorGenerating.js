function color(type) {
    let color;
    if (type === "grass") {
        color = `#74CB48`;
    }
    if (type === "fire") {
        color = `#F57D31`;
    }
    if (type === "water") {
        color = `#6493EB`;
    }
    if (type === "bug") {
        color = `#A7B723`;
    }
    if (type === "electric") {
        color = `#F9CF30`;
    }
    if (type === "poison") {
        color = `#A43E9E`;
    }
    if (type === "normal") {
        color = `#AAA67F`;
    }
    if (type === "ground") {
        color = `#DEC16B`;
    }
    if (type === "fighting") {
        color = `#C12239`;
    }
    if (type === "rock") {
        color = `#B69E31`;
    }
    if (type === "psychic") {
        color = `#FB5584`;
    }
    if (type === "dragon") {
        color = `#7037FF`;
    }
    if (type === "fairy") {
        color = `#E69EAC`;
    }

    return color;
}
